public class NodeForDoublyLinkedList {

    public int data;
    public NodeForDoublyLinkedList prev;
    public NodeForDoublyLinkedList next;


    public NodeForDoublyLinkedList(int data) {
        this.data = data;
        this.prev = null;
        this.next = null;
    }
}
